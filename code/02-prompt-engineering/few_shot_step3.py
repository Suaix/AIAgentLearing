# 任务 ①：改参数 — 探索 temperature 对分类一致性的影响
# ══════════════════════════════════════════════════════════════════════
#
# 目标：理解 temperature 参数如何影响模型在"确定性任务"上的表现
#
# 你要做的：
#   1. 预测：temperature=1.5 时，同一个句子跑 5 次，结果会全部一致吗？
#   2. 运行代码看实际结果
#   3. 把 temperature 改成 0.0，再跑一次，对比结果
#
# 预期输出：
#   temperature=1.5 → 5 次中可能出现不一致（正面/负面混出）
#   temperature=0.0 → 5 次结果完全相同
# ══════════════════════════════════════════════════════════════════════

def task1_temperature_experiment():
    print("=" * 60)
    print("任务 ①：temperature 对分类一致性的影响")
    print("=" * 60)

    system_prompt = """你是一个情感分析助手。判断以下句子的情感倾向。
输出格式：只输出「正面」「负面」「中性」三个词之一，不要解释。"""

    test_sentence = "这个产品，怎么说呢，买了之后我也不知道算不算值。"

    # ── 你的修改区域 ──────────────────────────────────────────
    # 第一次运行用 temperature=1.5，观察结果
    # 第二次改成 temperature=0.0，对比差异
    # ───────────────────────────────────────────────────────────

    temperatures = [1.5, 0.0]  # ← 试试不同值

    for temp in temperatures:
        print(f"\n🔬 temperature = {temp}")
        print("-" * 40)
        results = []
        for i in range(5):
            result = classify(system_prompt, test_sentence, temperature=temp)
            results.append(result)
            print(f"  第{i+1}次: {result}")

        # 统计一致性
        unique = set(results)
        if len(unique) == 1:
            print(f"  ✅ 5次结果完全一致: {unique.pop()}")
        else:
            print(f"  ⚠️ 出现了 {len(unique)} 种不同结果: {unique}")

    print('\n💡 思考：为什么 temperature=0.0 时结果一致？这和"确定性任务"有什么关系？')


